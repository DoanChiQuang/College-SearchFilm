    public ItemFilm(String id, String path, String title, String description) {
        this.id = id;
        this.path = path;
        this.title = title;
        this.description = description;
        initComponents();        
        try {
            titleTxt.setText(title);
            descriptionTxt.setText(title);
            String newPath = null;            
            if(!path.equals("null")) {
                newPath = APIController.imageURL + path;
                URL url = new URL(newPath);
                Image image = ImageIO.read(url);
                image = image.getScaledInstance(150,210,Image.SCALE_DEFAULT);
                image_handled.setIcon(new ImageIcon(image));
            }
            else {
                newPath = ".\\src\\assets\\icon\\noimage.jpg";
                Image image = ImageIO.read(new File(newPath));
                image = image.getScaledInstance(150,210,Image.SCALE_DEFAULT);
                image_handled.setIcon(new ImageIcon(image));
            }
        } catch (IOException e) {
            System.out.println("Error: " + e);
        }
    }

